#include "../libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*tmp;

	if (!n || !s)
		return ;
	tmp = (char *)s;
	while (--n)
		tmp[n] = '\0';
	tmp[n] = '\0';
}
